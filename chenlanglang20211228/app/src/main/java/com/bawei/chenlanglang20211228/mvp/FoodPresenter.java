    public FoodPresenter(FoodCon.FoodConModel mModel, FoodCon.FoodConView mView) {
        super(mModel, mView);
    }

    public void getData(int page){
        mModel.doFoodConModel(new FoodCallBack() {
            @Override
            public void onSuccess(List<Food.DataBean> dataBeans) {
                mView.doFoodConView(dataBeans);
            }
        },page);
    }
